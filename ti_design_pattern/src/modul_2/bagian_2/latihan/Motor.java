package modul_2.bagian_2.latihan;

public class Motor {
    private String merk;
    private int tahun;

    // setter untuk merk
    public void setMerk(String merk) {
        this.merk = merk;
    }

    // getter untuk merk
    public String getMerk() {
        return merk;
    }

    // setter untuk tahun
    public void setTahun(int tahun) {
        this.tahun = tahun;
    }

    // getter untuk tahun
    public int getTahun() {
        return tahun;
    }

}
