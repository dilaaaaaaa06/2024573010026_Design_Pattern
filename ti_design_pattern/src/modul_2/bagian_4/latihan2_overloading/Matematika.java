package modul_2.bagian_4.latihan2_overloading;

class Matematika {

    // tambah 2 bilangan int
    int tambah(int a, int b) {
        return a + b;
    }

    // tambah 3 bilangan int
    int tambah(int a, int b, int c) {
        return a + b + c;
    }

    // tambah 2 bilangan double
    double tambah(double a, double b) {
        return a + b;
    }
}
